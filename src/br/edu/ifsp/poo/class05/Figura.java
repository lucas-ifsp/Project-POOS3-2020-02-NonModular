package br.edu.ifsp.poo.class05;

public class Figura {
    private Integer x;
    private Integer y;

    public Figura(Integer x, Integer y){
        this.x = x;
        this.y = y;
    }

    public Double getArea(){
        return 0.0;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
